    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
        //Error: habra codigos repetidos cuando se borre uno que no sea el ultimo.
        try{
            //Modifico el coche
            coche.setPrecio(textPrecio.getText());
            if (radioSi.isSelected()) {
                coche.setVendido(true);
            }else if(radioNo.isSelected()){
                coche.setVendido(false);
            }
            muestraMensaje("Datos cambiados");
            limpioFormulario();
        }catch(MyException ex){
            muestraError(ex.getMessage());
        }
    }//GEN-LAST:event_botonAceptarActionPerformed

    private void botonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonLimpiarActionPerformed
        limpioFormulario();
    }//GEN-LAST:event_botonLimpiarActionPerformed

    private void textCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textCodigoActionPerformed
        try {
            coche = gestora.GestorCoches.buscaCoche(textCodigo.getText());
            rellenoFormulario(coche);
        } catch (MyException ex) {
            muestraError(ex.getMessage());
        }
    }//GEN-LAST:event_textCodigoActionPerformed

    private void textPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textPrecioActionPerformed
        try{
            coche.setPrecio(textPrecio.getText());
            textPrecio.setText(coche.getPrecio()+"");
        }catch(MyException | IndexOutOfBoundsException ex){
            muestraError(ex.getMessage());
        }
        
    }//GEN-LAST:event_textPrecioActionPerformed

