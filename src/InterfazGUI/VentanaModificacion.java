    private void rellenoFormulario(Coche c){
        textCodigo.setEnabled(false);
        textPrecio.requestFocus();
        textModelo.setText(c.getModelo());
        textModelo.setEnabled(false);
        textPrecio.setText(c.getPrecio()+"");
        textPrecio.setEnabled(true);
        if (c.isVendido()) {
            radioSi.setSelected(true);
        }else{
            radioNo.setSelected(true);
        }
    }
    
    
    
    private void muestraMensaje(String mensaje){
        JOptionPane.showMessageDialog(this, mensaje, 
                "CPIFP", JOptionPane.INFORMATION_MESSAGE);
    }
    private void muestraError(String mensaje){
        JOptionPane.showMessageDialog(this, mensaje, 
                "CPIFP", JOptionPane.ERROR_MESSAGE);
    }
    
    private void limpioFormulario(){
        coche = new Coche();
        textCodigo.setText("");
        textCodigo.requestFocus();
        textAreaMuestra.setText("");
        textModelo.setText("");
        textPrecio.setText("");
        grupoVendido.clearSelection();
        textCodigo.setEnabled(true);
        textModelo.setEnabled(false);
        textPrecio.setEnabled(false);
    }

